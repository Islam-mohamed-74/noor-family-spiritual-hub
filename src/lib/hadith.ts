/**
 * Get a random hadith, optionally filtered by category.
 */
export function getRandomHadith(
  category?: HadithEntry["category"],
): HadithEntry {
  const pool = category
    ? HADITHS.filter((h) => h.category === category)
    : HADITHS;
  return pool[Math.floor(Math.random() * pool.length)];
}

/**
 * Get a deterministic daily hadith based on today's date.
 */
export function getDailyHadith(): HadithEntry {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) /
      86400000,
  );
  return HADITHS[dayOfYear % HADITHS.length];
}

/**
 * Get a random completion tip for a given activity type.
 */
export function getCompletionTip(
  type: keyof typeof COMPLETION_TIPS = "general",
): string {
  const pool = COMPLETION_TIPS[type] ?? COMPLETION_TIPS.general;
  return pool[Math.floor(Math.random() * pool.length)];
}
